public class switchcase{
public static void main(String[] args) {
	
	int fahim =3;
	
		switch(fahim){
		case 1:{
		
			System.out.println("he is a good boy");
			break;
		}
		case 2:{
		
			System.out.println("he is not a good boy");
			break;
		}
		default:{
			System.out.println("he is a dongrae");
		}
		}
		}
}
